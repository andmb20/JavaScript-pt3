/*
    Operadores de Comparação (Comparison Operators)

    > = Maior que
    < = Menor que

    >= -> Maior ou Igual que
    <= -> Menor ou Igual que
*/

const firstNumber = 20
const seccondNumber = 30

console.log(firstNumber > seccondNumber) //Maior que 

console.log(firstNumber < seccondNumber) //Menor que

console.log(firstNumber >= seccondNumber) //Maior ou Igual que

console.log(firstNumber <= seccondNumber) //Menor ou Igual que