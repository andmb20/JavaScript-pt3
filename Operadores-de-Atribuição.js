/*
    Operadores de Atribuição (Assignment Operators)
    = -> Atribuição (Assignment)
   += -> Adição (Addition)
   -= -> Subtração (Subtraction)
   /= -> Divisão (Division)
   *= -> Multiplicação (Multiplication)
   %= -> Resto (Modulus)

*/

let addition = 30
addition += 20 // addition = addition + 20
console.log(addition)

let subtraction = 30
subtraction -= 20 // subtraction = subtraction - 20
console.log(subtraction)

let division = 30
division /= 6 // division = division / 6
console.log(division)

let multiplication = 30
multiplication *= 800 // multiplication = multiplication * 20
console.log(multiplication)

let modulus = 30
modulus %= 18 // modulus = modulus % 20
console.log(modulus)
